def celsius_fahrenheit(c: float) -> float:
    """Converte  tempratura em Celsius para Fahrenheit.

    Formula: F = C * 9/5 + 32
    """
    return c * 9 / 5 + 32

def metros_quilometros (m : float) -> float:
    """Converte distancia em metros para quilometros.

    1 km = 1000 m
    """
    return m / 1000

